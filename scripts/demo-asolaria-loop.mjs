#!/usr/bin/env node
/**
 * demo-asolaria-loop.mjs — reproducible 5-iteration "Simplicio on Metal" demo.
 *
 * Issue #78 asked for a demo loop that shows Simplicio "improving its own
 * runtime" the way Asolaria's ASI-on-Metal narrative frames self-evolution.
 * This script does NOT fabricate an ASI claim. It runs five real,
 * reproducible orientation iterations — one per canonical map named in
 * SIMPLICIO-MAP-OF-MAPS.md — and measures, with the repo's existing
 * labeled heuristic (`heuristic:chars-div-4`, same as
 * lib/observability/savings.ts and scripts/token-budget.mjs), how many
 * tokens an operator/agent would need to read to get oriented:
 *
 *   baseline = reading the full underlying spec/domain file cold
 *   actual   = reading only the one-line map-of-maps summary row first
 *
 * That is the same "reductions" claim REDUCTIONS.md already makes for
 * runtime context (10KB of code -> 2KB of map), applied five times over
 * five different maps, which satisfies the "5 iterations" ask honestly:
 * every number here is derived from files that exist in this repo, not
 * from a simulated agent run.
 *
 * Usage:
 *   node scripts/demo-asolaria-loop.mjs            # print + write report
 *   node scripts/demo-asolaria-loop.mjs --check     # exit 1 if <5 iterations
 *
 * Output: prints a human table to stderr and writes a receipt to
 * .specs/strategy/campaigns/2026-Q3-asolaria-on-metal/DEMO-RUN.md
 */

import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const ESTIMATOR = "heuristic:chars-div-4";
const REPORT_PATH = join(
  ROOT,
  ".specs",
  "strategy",
  "campaigns",
  "2026-Q3-asolaria-on-metal",
  "DEMO-RUN.md",
);

// Five iterations, one per canonical map row in SIMPLICIO-MAP-OF-MAPS.md.
const ITERATIONS = [
  {
    name: "orient:product",
    map_row: "VISION",
    baseline: [".specs/product/VISION.md", ".specs/product/DOMAIN.md"],
    map_summary: "Product promise, non-goals, and domain rules.",
  },
  {
    name: "orient:architecture",
    map_row: "DESIGN",
    baseline: [".specs/architecture/DESIGN.md"],
    map_summary: "System architecture backbone behind marketing claims.",
  },
  {
    name: "orient:providers",
    map_row: "PROVIDERS",
    baseline: [".specs/architecture/PROVIDERS.md"],
    map_summary: "Routing contract for vendor swaps.",
  },
  {
    name: "orient:strategy",
    map_row: "PLAYBOOKS",
    baseline: [".specs/strategy/PLAYBOOKS.md"],
    map_summary: "Channel-specific launch behavior.",
  },
  {
    name: "orient:operator",
    map_row: "OPERATOR",
    baseline: ["docs/OPERATOR.md", "docs/architecture-map.md"],
    map_summary: "Loop/operator contract and maintainer repo map.",
  },
];

function estimate(text) {
  return Math.ceil(text.length / 4);
}

function readIfExists(rel) {
  const p = join(ROOT, rel);
  return existsSync(p) ? readFileSync(p, "utf8") : "";
}

function runIteration(iter, index) {
  const baselineText = iter.baseline.map(readIfExists).join("\n");
  const baselineTokens = estimate(baselineText);
  const actualTokens = estimate(iter.map_summary);
  const saved = Math.max(0, baselineTokens - actualTokens);
  const pct = baselineTokens > 0 ? Math.round((saved / baselineTokens) * 1000) / 10 : 0;
  return {
    iteration: index + 1,
    name: iter.name,
    map_row: iter.map_row,
    baseline_files: iter.baseline,
    tokens: {
      baseline_total: baselineTokens,
      actual_total: actualTokens,
      saved_total: saved,
      pct_saved: pct,
    },
  };
}

function renderReport(results) {
  const lines = [];
  lines.push("# DEMO RUN — Simplicio on Metal (5-iteration reproducible demo)");
  lines.push("");
  lines.push("Generated by `node scripts/demo-asolaria-loop.mjs`. Re-run any time —");
  lines.push("this file is a receipt, not hand-authored copy.");
  lines.push("");
  lines.push(`Estimator: \`${ESTIMATOR}\` (same labeled heuristic as REDUCTIONS.md and lib/observability/savings.ts).`);
  lines.push("");
  lines.push("| # | Iteration | Map row | Baseline files (cold read) | Baseline tok | Map-row tok | Saved | % saved |");
  lines.push("|---|---|---|---|---:|---:|---:|---:|");
  for (const r of results) {
    lines.push(
      `| ${r.iteration} | ${r.name} | ${r.map_row} | ${r.baseline_files.join(", ")} | ${r.tokens.baseline_total} | ${r.tokens.actual_total} | ${r.tokens.saved_total} | ${r.tokens.pct_saved}% |`,
    );
  }
  const totalBaseline = results.reduce((s, r) => s + r.tokens.baseline_total, 0);
  const totalActual = results.reduce((s, r) => s + r.tokens.actual_total, 0);
  const totalSaved = totalBaseline - totalActual;
  const totalPct = totalBaseline > 0 ? Math.round((totalSaved / totalBaseline) * 1000) / 10 : 0;
  lines.push("");
  lines.push(
    `**Totals across 5 iterations:** ${totalBaseline} tok baseline -> ${totalActual} tok via the map row (${totalSaved} tok saved, ${totalPct}%).`,
  );
  lines.push("");
  lines.push("## What this proves");
  lines.push("");
  lines.push("- Every number above is measured from files that exist in this repo right now.");
  lines.push("- The claim is scoped to *orientation reading cost*, not to an autonomous agent");
  lines.push("  rewriting its own runtime — this repo does not claim that.");
  lines.push("- `proof.kind` for this style of number is always `estimated` (heuristic chars/4),");
  lines.push("  matching the honesty discipline in lib/observability/savings.ts.");
  lines.push("");
  lines.push("## What is still external");
  lines.push("");
  lines.push("- a recorded screen capture or Remotion/Hyperframes asset of this script running");
  lines.push("- a hosted MP4/GIF for the public demo page");
  lines.push("- a public landing URL to link the recorded demo from");
  lines.push("");
  return lines.join("\n");
}

function main() {
  const checkMode = process.argv.includes("--check");
  const results = ITERATIONS.map(runIteration);

  process.stderr.write("demo-asolaria-loop: 5-iteration reproducible orientation demo\n");
  for (const r of results) {
    process.stderr.write(
      `  [${r.iteration}/5] ${r.name} :: baseline=${r.tokens.baseline_total}tok actual=${r.tokens.actual_total}tok saved=${r.tokens.pct_saved}%\n`,
    );
  }

  if (checkMode) {
    if (results.length < 5) {
      process.stderr.write(`demo-asolaria-loop: FAIL — expected 5 iterations, got ${results.length}\n`);
      process.exit(1);
    }
    process.stderr.write("demo-asolaria-loop: PASS (5 iterations present)\n");
    return;
  }

  const report = renderReport(results);
  writeFileSync(REPORT_PATH, `${report}\n`);
  process.stderr.write(`demo-asolaria-loop: wrote ${REPORT_PATH}\n`);
}

main();
